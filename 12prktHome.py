import turtle as t
import random as r

stars = int(input("Введите количество звезд: "))
t.speed(500)
t.bgcolor('black')
# Травка
t.up()
t.goto(-500, -150)
t.color('green')
t.down()
t.fillcolor('green')
t.begin_fill()
t.forward(1000)
t.right(90)
t.forward(200)
t.right(90)
t.forward(1000)
t.right(90)
t.forward(200)
t.end_fill()
# Домик
t.up()
t.goto(0, -150)
t.right(90)
t.forward(150)
t.left(90)
t.color('orange')
t.down()
t.fillcolor('orange')
t.begin_fill()
t.forward(200)
t.left(90)
t.forward(300)
t.left(90)
t.forward(200)
t.left(90)
t.forward(300)
t.end_fill()
# Крыша
t.up()
t.goto((160, 50))
t.left(135)
t.color('sienna')
t.down()
t.fillcolor('sienna')
t.begin_fill()
t.forward(160 * (2 ** 0.5))
t.left(90)
t.forward(160 * (2 ** 0.5))
t.left(135)
t.forward(320)
t.end_fill()
# Дверь
t.up()
t.goto(-30, -40)
t.color('brown')
t.down()
t.fillcolor('brown')
t.begin_fill()
t.forward(60)
t.right(90)
t.forward(110)
t.right(90)
t.forward(60)
t.right(90)
t.forward(110)
t.end_fill()
# Окно (левое)
t.up()
t.goto(-120,-20)
t.right(90)
t.color('blue')
t.down()
t.fillcolor('blue')
t.begin_fill()
t.forward(40)
t.right(90)
t.forward(60)
t.right(90)
t.forward(40)
t.right(90)
t.forward(60)
t.end_fill()
# Окно (правое)
t.up()
t.goto(120, -20)
t.left(90)
t.down()
t.fillcolor('blue')
t.begin_fill()
t.forward(40)
t.left(90)
t.forward(60)
t.left(90)
t.forward(40)
t.left(90)
t.forward(60)
t.end_fill()
# Звездочки
for i in range(stars):
    # Выбор места
    t.up()
    t.color('black')
    pos = r.randint(1, 5)
    if pos == 1:  # Правая область
        t.goto(r.uniform(175, 350), r.uniform(-140, 290))
    elif pos == 2:  # Верхняя область
        t.goto(r.uniform(-170, 170), r.uniform(220, 290))
    elif pos == 3:  # Левая область
        t.goto(r.uniform(-350, -175), r.uniform(-140, 290))
    elif pos == 4:  # Правый уголок
        x1 = r.uniform(10, 170)
        y1 = r.uniform(60, 220)
        if (abs(x1) + (abs(y1) - 50)) > 175:
            t.goto(x1, y1)
        else:
            t.goto(r.uniform(175, 350), r.uniform(-140, 290))
    elif pos == 5:  # Левый уголок
        x2 = r.uniform(-10, -170)
        y2 = r.uniform(60, 220)
        if (abs(x2) + (abs(y2) - 50)) > 175:
            t.goto(x2, y2)
        else:
            t.goto(r.uniform(-350, -175), r.uniform(-140, 290))
    # Появление звезды
    t.color('yellow')
    t.down()
    t.fillcolor('yellow')
    t.begin_fill()
    for j in range(5):
        t.forward(20)
        t.right(144)
    t.end_fill()

t.mainloop()
